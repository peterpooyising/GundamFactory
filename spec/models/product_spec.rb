require 'rails_helper'

RSpec.describe Product, type: :model do

  let(:subject) { create(:product) }

  it { is_expected.to belong_to(:brand) }
  it { is_expected.to have_many(:category) }
  it { is_expected.to validate_presence_of(:name) }
  it { is_expected.to validate_uniqueness_of(:name) }
  it { is_expected.to validate_presence_of(:brand) }
  it { is_expected.to validate_presence_of(:category) }
  it { is_expected.to validate_presence_of(:cost_price) }
  it { is_expected.to validate_presence_of(:selling_price) }
  it { is_expected.to validate_presence_of(:stock) }

end
